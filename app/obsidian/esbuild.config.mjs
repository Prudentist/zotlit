// Read the ACTUALLY-RESOLVED better-sqlite3 package version from node_modules.
// This is the JS code esbuild is about to bundle into main.js; WiseLibs
// publishes GitHub release tags + prebuilt .tar.gz filenames in lockstep with
// the npm version, so this string is guaranteed to match the download URL.
// Reading the resolved package (not our own package.json spec) means caret
// ranges, workspace protocols, and transitive resolutions all "just work".
import betterSqlite3Pkg from "better-sqlite3/package.json" with { type: "json" };
import PostcssPlugin from "./esbuild-postcss.mjs";

const myPackage = JSON.parse(await readFile("./package.json"));
const isPreRelease = semverPrerelease(myPackage.version) !== null;

const isProd = process.env?.BUILD === "production";

// In PROD only: fetch the WiseLibs release for the resolved version and derive
// a { [modules]: { [platform]: arch[] } } support matrix from its prebuilt
// asset filenames. This replaces the hand-maintained app/obsidian/src/platform.json.
// Fails the build on any error — Octokit throws on non-2xx, and we also throw
// if the asset-name parse produces an empty matrix. We never ship a bundle
// whose runtime platform check runs against absent/stale data.
//
// In DEV: skip the fetch entirely. Inject an empty matrix {} — the runtime
// helpers treat that as "skip the pre-flight, trust the download" and fall
// through to the existing 404-fallback in guide/auto.tsx.
const betterSqlite3SupportMatrix = isProd
  ? await (async () => {
      const releaseTag = `v${betterSqlite3Pkg.version}`;
      const octokit = new Octokit({
        auth: process.env.GITHUB_TOKEN, // undefined = unauthenticated, 60/hr
        userAgent: "zotlit-build",
      });
      const { data: release } = await octokit.repos.getReleaseByTag({
        owner: "WiseLibs",
        repo: "better-sqlite3",
        tag: releaseTag,
      });
      const assetRe = new RegExp(
        `^better-sqlite3-${releaseTag}-electron-v(\\d+)-([^-]+)-([^.]+)\\.tar\\.gz$`,
      );
      const matrix = {};
      for (const asset of release.assets) {
        const m = asset.name.match(assetRe);
        if (!m) continue;
        const [, modules, platform, arch] = m;
        ((matrix[modules] ??= {})[platform] ??= []).push(arch);
      }
      if (Object.keys(matrix).length === 0) {
        throw new Error(
          `WiseLibs release ${releaseTag} has no electron prebuilt assets — ` +
            `either the version is wrong or WiseLibs changed their asset naming convention`,
        );
      }
      console.log(
        `better-sqlite3 ${releaseTag} prebuilt electron releases:`,
      );
      for (const [modules, platforms] of Object.entries(matrix)) {
        for (const [platform, archs] of Object.entries(platforms)) {
          console.log(`  modules=${modules} ${platform}: ${archs.join(", ")}`);
        }
      }
      return matrix;
    })()
  : {};

